					<p>The RAW/WAR project is generated from the idea that history is about access and authorship and that we, as a global digital community, can now all participate in its creation and change the way history itself is constructed. Using new technologies, current and future generations can create their own histories, breaking the cycle of omission and erasure.</p>

<p>RAW/WAR takes the form of an interactive, community-curated archive and an accompanying installation that provide a forum in which users can come together, share their stories and collaboratively contribute to the history of women’s art. 
</p>

<p>The project emerges from the documentary <a href="http://womenartrevolution.com/" target="new">!Women Art Revolution</a>. While the film provides a personal perspective of feminist activism in a national context, RAW/WAR opens up this dialogue to a global audience, connecting women and their histories worldwide. </p>

<p>An ongoing partnership with Stanford University Libraries (SULAIR) houses the <a href="http://lib.stanford.edu/women-art-revolution	" target="new">!Women Art Revolution Collection</a> in a publicly accessible online archive for study and research. The collection, acquired in 2008, holds the interview footage and transcripts from the film. Because of the retrievability of this information, there are no outtakes, subverting traditional notions of filmmaking. RAW/WAR is about continuing this ongoing lineage into the future and allowing users to add, and ultimately, remix, their own stories. </p>

<p>RAW/WAR is self-curated. This is achieved by rating and meta-rating and is intricately linked to the whole thesis of !WAR: shedding light on who is left out and why.</p> 
<p>
The RAW/WAR installation is a live participatory environment that allows users to “bring light” to the lost or invisible histories of women in art with virtual flashlight controllers accessing the interactive, community-curated archive.</p>
<p>
The RAW/WAR project is a project by <a href="http://lynnhershman.com/" target="new">Lynn Hershman Leeson</a>, in collaboration with <a href="http://painteralex.com/" target="new">Alexandra Chowaniec</a>, <a href="http://chirls.com/" target="new">Brian Chirls</a>, <a href="http://villamil.org" target="new">Gian Pablo Villamil</a> and <a href="http://paradisoprojects.com/" target="new">Paradiso Projects</a>.</p>
